function y = y2(x,c)

Ax = [x, x^2/2, x^3/3, x^4/4;...
    x^2/2, x^3/3, x^4/4, x^5/5;...
    x^3/3, x^4/4, x^5/5, x^6/6;...
    x^4/4, x^5/5, x^6/6, x^7/7];

cx = [c(1,1)*c(1,2), c(1,1)*c(2,2), c(1,1)*c(3,2), c(1,1)*c(4,2);...
    c(2,1)*c(1,2), c(2,1)*c(2,2), c(2,1)*c(3,2), c(2,1)*c(4,2);...
    c(3,1)*c(1,2), c(3,1)*c(2,2), c(3,1)*c(3,2), c(3,1)*c(4,2);...
    c(4,1)*c(1,2), c(4,1)*c(2,2), c(4,1)*c(3,2), c(4,1)*c(4,2)];

y = sum(sum(Ax.*cx));
                
end

